CREATE  procedure [dbo].[MG_UPDATEMG_TeDelete]
(
@PNRD INT,
@PSGN INT
)
AS

-- Permiresohet se ku eshte rasti FDSCR

UPDATE LMG
   SET LMG.SASI  = LMG.SASI +(FHSCR.SASI*(@PSGN)), 
       LMG.VLERE = LMG.VLERE+(FHSCR.VLERAM*(@PSGN))
  FROM LMG LEFT JOIN FHSCR ON LMG.KOD = FHSCR.KOD 
WHERE FHSCR.NRD=@PNRD



GO
